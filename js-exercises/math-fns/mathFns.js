
// Don't change the export values.
// You can use the function in the Math module
function sqrt(squaredNumber){
  return Math.sqrt(squaredNumber)
}
function power(base,exponent){
  return Math.pow(base,exponent)
}
function round(floatNumber){
  return Math.round(floatNumber)
}
export {
  sqrt, 
  power,
  round,
};
